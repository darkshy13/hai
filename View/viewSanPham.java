    void add() {
        SanPham sp = new SanPham();
        int id = Integer.parseInt(txtIdSP.getText());
        String ten = txtTenSP.getText();
        float giaSP = Float.parseFloat(txtGiaSP.getText());
        String moTa = txtGiaSP.getText();
      
        int khuyenMai = Integer.parseInt(txtIDKhuyenMai.getText());

        sp.setiSanPham(id);
        sp.setTenSP(ten);
        sp.setGiaSP(giaSP);
        sp.setMoTaSp(moTa);
     
        sp.setIdKhuyenMai(khuyenMai);

        boolean add = service.add(sp);
        if (add) {
            JOptionPane.showMessageDialog(this, "them thanh cong");
            list2 = service.getAll(1, 10);
            fillTable(list2);
        } else {
            JOptionPane.showMessageDialog(this, "fall");
        }
    }
