 void showSanPham(int index) {
        ArrayList<SanPham> spList = service.getAll(1, 10);
        if (index >= 0 && index < spList.size()) {
            SanPham sp = spList.get(index);
            txtIdSP.setText(String.valueOf(sp.getiSanPham()));
            txtTenSP.setText(sp.getTenSP());
            txtGiaSP.setText(String.valueOf(sp.getGiaSP()));
            txtMoTa.setText(sp.getMoTaSp());
        
            txtIDKhuyenMai.setText(String.valueOf(sp.getIdKhuyenMai()));

        }

    }
